#include<iostream>
using namespace std;
int main(){

    //left shift
    //3<<2-->3 ko do baar left shift karo-->0011 changes to 1100
    //small number get double ,large get negative
     cout<<(17<<1)<<endl;
     cout<<(17<<2)<<endl;


    //right shift
    //5>>2 -->101 changes to 001  do bahaar chale gaye
    cout<<(19>>1)<<endl;
    cout<<(19>>2)<<endl;
}
//Bitwise (&) is bitwise operation and && means logical ie 0/1(1 bit)
#include<iostream>
using namespace std;
int main(){
     int a=4;
     int b=6;

     cout<<"a&b: " << (a&b) <<endl;
     cout<<"a|b: " << (a|b) <<endl;
     cout<<"~a: " << ~a <<endl;
     cout<<"a^b: " << (a^b)<<endl;
     return 0;

}